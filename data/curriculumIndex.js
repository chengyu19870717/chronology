function parseRow(line) {
  const [name, category, focus, lifeText = '生卒年不详'] = line.split('|');
  if (/世纪/.test(lifeText)) return { name, category, focus, lifeText, birthYear: null, deathYear: null };
  const match = lifeText.match(/(?:约)?(前)?(\d{1,4}|[？?])\s*[-—–至]\s*(?:约)?(前)?(\d{1,4}|[？?])/);
  if (!match) return { name, category, focus, lifeText, birthYear: null, deathYear: null };
  const toYear = (value, isBce) => /[？?]/.test(value) ? null : Number(value) * (isBce ? -1 : 1);
  return {
    name,
    category,
    focus,
    lifeText,
    birthYear: toYear(match[2], match[1]),
    deathYear: toYear(match[4], match[3]),
  };
}

function inferDynastyIds(seed, primaryId) {
  const ids = (CROSS_DYNASTY_OVERRIDES[seed.name] || [primaryId]).slice();
  if (seed.birthYear !== null && seed.deathYear !== null) {
    const activeStart = seed.birthYear + 15;
    DYNASTY_TRANSITIONS.forEach(([beforeId, afterId, year]) => {
      if (activeStart > year || seed.deathYear < year) return;
      if (ids.indexOf(beforeId) === -1) ids.push(beforeId);
      if (ids.indexOf(afterId) === -1) ids.push(afterId);
    });
  }
  if (ids.indexOf(primaryId) === -1) ids.push(primaryId);
  return Object.keys(GROUP_META).filter(id => ids.indexOf(id) !== -1);
}

function relationshipProfile(first, second) {
  const categories = `${first.category} ${second.category}`;
  if (/将领|军事|起义|武将|抗英|抗法|抗倭/.test(first.category)
    && /将领|军事|起义|武将|抗英|抗法|抗倭/.test(second.category)) {
    return { type: '同朝军事人物对照', angle: '军队组织、战略目标与战争后果' };
  }
  if (/思想|学者|文学|史学|经学|诗人|词人|小说|戏曲|书画|画家/.test(first.category)
    && /思想|学者|文学|史学|经学|诗人|词人|小说|戏曲|书画|画家/.test(second.category)) {
    return { type: '同领域思想文化对照', angle: '知识方法、作品传播与时代议题' };
  }
  if (/科学|科技|医学|天文|数学|水利|工程|农学|地理|历法|工艺/.test(first.category)
    && /科学|科技|医学|天文|数学|水利|工程|农学|地理|历法|工艺/.test(second.category)) {
    return { type: '同领域科技实务对照', angle: '问题来源、技术方法与成果应用' };
  }
  if (/君主|国君|首领|宗室|诸侯王|王室/.test(first.category)
    && /君主|国君|首领|宗室|诸侯王|王室/.test(second.category)) {
    return { type: '同代统治人物对照', angle: '权力来源、政权建设与危机决策' };
  }
  if (/外交|使者|和亲|交流|传播|商人|航海/.test(categories)) {
    return { type: '同代交流网络对照', angle: '信息、路线、授权与跨区域互动' };
  }
  return { type: '同一时代脉络', angle: '正式身份、资源条件与公共行动' };
}

