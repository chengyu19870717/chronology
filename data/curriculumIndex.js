function normalize(value) {
  return String(value || '').toLowerCase().replace(/[·•\s]+/g, '');
}

function searchPeople(keyword) {
  const query = normalize(keyword);
  if (!query) return people.slice();
  return people.filter(item => (
    normalize([
      item.name,
      item.categoryText,
      item.dynastyName,
      item.periodLabel,
      item.crossText,
      item.activePeriodText,
      item.focus,
      item.summary,
    ].join(' ')).includes(query)
    || personPinyinScore(item.id, query) < 9
  )).sort((a, b) => personPinyinScore(a.id, query) - personPinyinScore(b.id, query));
}

function searchEvents(keyword) {
  const query = normalize(keyword);
  if (!query) return events.slice();
  return events.filter(item => normalize([
    item.name,
    item.dynastyName,
    item.periodLabel,
    item.dynastyIds.map(id => GROUP_META[id].dynastyName).join(' '),
    item.tagText,
    item.summary,
  ].join(' ')).includes(query));
}

function getDynastyExpansion(dynastyId) {
  const meta = GROUP_META[dynastyId];
  if (!meta) return null;
  return {
    id: `curriculum-${dynastyId}`,
    name: '教材与通史扩展',
    dateText: meta.dateText,
    ruler: '政治、社会、文化、科技与交流专题',
    people: people.filter(item => item.dynastyIds.indexOf(dynastyId) !== -1),
    events: events.filter(item => item.dynastyIds.indexOf(dynastyId) !== -1),
    personTotal: people.filter(item => item.dynastyIds.indexOf(dynastyId) !== -1).length,
    eventTotal: events.filter(item => item.dynastyIds.indexOf(dynastyId) !== -1).length,
    isCurriculum: true,
  };
}

function getPeopleMentioning(name) {
  const query = String(name || '').replace(/（.*?）/g, '').trim();
  if (!query) return [];
  return people.filter(item => item.name !== query && item.focus.indexOf(query) !== -1);
}

