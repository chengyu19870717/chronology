function getDynastyTagInfo(label, dynastyName) {
  const entry = TAG_GLOSSARY[label];
  if (!entry) return null;
  return {
    label,
    dynastyName,
    category: entry.category,
    definition: entry.definition,
    context: entry.context,
    note: entry.note || DEFAULT_NOTE,
  };
}

function hasDynastyTagInfo(label) {
  return Boolean(TAG_GLOSSARY[label]);
}

