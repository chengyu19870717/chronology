function getPersonTagInfo(label, personName, periodText) {
  const normalized = String(label || '').trim();
  if (!normalized) return null;
  const exact = EXACT_GLOSSARY[normalized];
  const pattern = PATTERN_GLOSSARY.find(item => item[0].test(normalized));
  const matched = exact || (pattern ? pattern.slice(1) : null);
  const info = matched || [
    '人物标签',
    `“${normalized}”用于概括人物在本表中的主要身份、活动领域、时代位置或评价边界。`,
    '该标签用于检索和速查，不替代人物履历、官职权限、具体事件与史料说明。',
  ];
  const person = personName || '该人物';
  const period = periodText ? `，其主要活动时期为${periodText}` : '';
  return {
    label: normalized,
    category: info[0],
    personName: person,
    definition: info[1],
    context: `${person}被标注为“${normalized}”${period}。${info[2]}`,
    note: '人物标签是速查入口，不是对人物功过的最终结论；请结合履历、关系、事件和争议页签阅读。',
  };
}

function hasPersonTagInfo(label) {
  return !!getPersonTagInfo(label);
}

